import type { NextPage } from "next";
import Head from "next/head";
import Image from "next/image";
import { useState, useEffect } from "react";
import detectEthereumProvider from "@metamask/detect-provider";
import Web3 from "web3";
import { ethers } from "ethers";
import Link from "next/link";
import Top from "./Top";
import connectWalletImage from "../public/index/connectWalletButton.svg";
import sub from "../public/index/Subtract.svg";

const Home: NextPage = () => {
  const [account, setAccount] = useState<string>("");
  const connectWallet = () => {
    if (typeof globalThis.window?.ethereum !== "undefined") {
      window.ethereum
        .request({ method: "eth_requestAccounts" })
        .then((accounts) => {
          setAccount(accounts[0]);
        });
    } else {
      console.log("MetaMask is NOT Installed!!");
    }
  };
  return (
    <div>
      <Head>
        <title>zkScore</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <div>
          {account !== "" ? (
            <Top />
          ) : (
            // TODO 画像を重ねる
            <div className="h-screen w-screen flex justify-center items-center relative">
              <Image
                src={sub}
                alt="connect"
                width={500}
                height={500}
                className="relative"
              />
              <Image
                src={connectWalletImage}
                alt="connect"
                onClick={connectWallet}
                className="absolute z-50 left-72"
              />
            </div>
          )}
        </div>
      </main>
    </div>
  );
};

export default Home;
